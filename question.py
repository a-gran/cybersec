# Функция показывает один вопрос: номер, статус и текст сообщения
# question_num — номер текущего вопроса (например, 3)
# case — словарь с данными вопроса (текст, правильный ответ, объяснение)
# total — сколько вопросов всего в игре
# lives — сколько жизней осталось у игрока прямо сейчас
# score — сколько очков набрал игрок прямо сейчас
def show_question(question_num, case, total, lives, score):
    print("==========================================================================")
    print(f"Вопрос: {question_num}/{total}   |   Жизни: {lives}   |   Счет: {score}")
    print("==========================================================================")

    print("\nСообщение:")
    print(case["text"])


def get_choice():
    while True:
        user_input = input("Введите '1' (скам) или '2' (безопасно): ").strip()
        if user_input == "1":
            return True
        elif user_input == "2":
            return False
        else:
            print("Некорректный ввод. Введите 1 или 2.")
