# Берём константу POINTS из config — она нужна для начисления очков и подсчёта процента
from config import POINTS

# Функция проверяет ответ игрока и обновляет счёт, жизни и количество правильных ответов
# player — ответ игрока: True (думает скам) или False (думает безопасно)
# case — словарь с данными вопроса, в том числе правильным ответом case['is_scam']
# lives, score, correct — текущие значения жизней, очков и правильных ответов
def process_answer(player, case, lives, score, correct):
    # Сравниваем ответ игрока с правильным ответом из словаря
    if player == case['is_scam']:
        print('ПРАВИЛЬНО!')
        score += POINTS   # прибавляем очки за правильный ответ
        correct += 1      # считаем ещё один правильный ответ
    else:
        lives -= 1        # забираем одну жизнь за ошибку
        print('НЕПРАВИЛЬНО!')
    print()
    print('Объяснение:', case['explanation'])  # показываем пояснение из словаря по ключу 'explanation'
    print()
    # Возвращаем три обновлённых значения обратно в game_loop
    return lives, score, correct


# Функция считает итоговый титул игрока в зависимости от его результата
# score — итоговый счёт, total — количество вопросов
def final_title(score, total):
    # Считаем долю набранных очков от максимально возможных (total * POINTS)
    percent = score / (total * POINTS)
    # Если набрал 80% или больше — отличный результат
    if percent >= 0.8:
        return 'КИБЕРДЕТЕКТИВ — отличный результат!'
    # Если набрал от 50% до 79% — неплохо
    if percent >= 0.5:
        return 'ОСТОРОЖНЫЙ ПОЛЬЗОВАТЕЛЬ — неплохо, но есть куда расти.'
    # Иначе — новичок, набрал меньше половины
    return 'НОВИЧОК — не расстраивайся, теперь ты знаешь больше!'

